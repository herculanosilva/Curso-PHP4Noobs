Comentários em PHP

Um comentário no código PHP é uma linha que não é executada como parte do programa. Seu único propósito é ser lido por alguém que está olhando o código.

Os comentários podem ser usados ​​para:

    Deixe que outras pessoas entendam seu código
    Lembrar do que você estava pensando quando escreveu o código

Exemplos

Sintaxe para comentários de uma linha:

<!DOCTYPE html>
<html>
<body>

<?php
// Este é um comentário de uma linha 

# Este também é um comentário de uma linha
?>

</body>
</html>

Sintaxe para comentários de várias linhas:

<!DOCTYPE html>
<html>
<body>

<?php

/*
Este é um bloco de comentário
de várias linhasque se estende
por vários linhas
*/

?>

</body>
</html>

